package com.hospit.app.rest.email;


/**
 * @author: Artem Korzhan
 * @version: 1.0
 * The {@code CredentialsConstants} class defines constants for common email credential properties.
 * It provides static final strings for the password and user properties used in email configuration.
 */
public class CredentialsConstants {

    /**
     * Constant representing the property key for the email password.
     */
    public static final String PASSWD = "mail.smtp.password";

    /**
     * Constant representing the property key for the email user.
     */
    public static final String USER = "mail.smtp.user";
}

